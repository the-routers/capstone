	<h2>About</h2>
		<p>Jane is a nurse with experience in Forensics, ER/Trauma, Burn, and Eating Disorders, among various other specialties. Jane came to Albuquerque to complete a travel nursing assignment that comes to an end next month. She is looking to use her remaining free time to explore parts of Albuquerque that she has yet to see. </p>

		<p>Jane has traveled to 17 countries, mostly alone, and enjoys exploring the culture in the countries she has visited. However, Jane rarely finds time to enjoy local attractions and activities while living in the States. She has often felt that, “there will always be time to do local things.” However, in the cities that Jane has lived in as a travel nurse, taking time out to enjoy her city is rare.</p>

		<p>On Jane’s bucket list is to one day drive the entire route of Hwy 66 from Chicago to L.A. to and explore the many sites along the way. And, while Jane is in Albuquerque, she decides to take a day or two to explore the landmarks on Route 66 within the Albuquerque city limits.</p>

